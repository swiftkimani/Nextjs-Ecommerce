"use client"
import FormHeader from '@/components/backOffice/FormHeader';
import ImageInput from '@/components/Forminput/Imageinput';
import SelectInput from '@/components/Forminput/Selectinput';
import SubmitButton from '@/components/Forminput/SubmitButton';
import TextareaInput from '@/components/Forminput/TexrAreainput';
import TextInput from '@/components/Forminput/Textinput';
import { makePostRequest } from '@/lib/apiRequest';
import { generateSlug } from '@/lib/generateSlug';
import React, { useState } from 'react'
import { useForm } from 'react-hook-form';

export default function NewCategory() {

  const markets = [
    {
      id: 1,
      title: "headphones Market",
    },
    {
      id: 2,
      title: "PowerBanks Market",
    },
    {
      id: 3,
      title: "Sunglasses Market",
    },
    {
      id: 4,
      title: "electronics Market",
    },
    {
      id: 5,
      title: "Chargers Market",
    },
  ];

  const [imageUrl, setImageUrl] = useState("")
  const [loading, setLoading] = useState(false)
  const { register, reset, handleSubmit, formState: { errors } } = useForm()
  
  async function onSubmit(data) {
    
    const slug = generateSlug(data.title)
    data.slug = slug
    data.imageUrl = imageUrl
    console.log(data)

    makePostRequest(setLoading, "api/categories", data, "Category", reset); 

    setImageUrl("")

  }
  return (
    <div>
      <FormHeader title="New Catergory" />
      {/* -id(autogenerated), -title, -slug(autogenerated), -description, -image */}

      <form
        onSubmit={handleSubmit(onSubmit)}
        className="w-full max-w-4xl p-4 bg-white border border-gray-200 rounded-lg shadow sm:p-6 md:p-8 dark:bg-gray-800 dark:border-gray-700 mx-auto my-3 ">
        <div className="grid gap-4 sm:grid-cols-2 sm:gap-6">
          <TextInput
            label="Category Title"
            name="title"
            register={register}
            errors={errors}
            className="w-full"
          />
          <SelectInput
            label="Select Markets"
            name="marketIds"
            register={register}
            errors={errors}
            className="w-full"
            options={markets}
            multiple={true}
          />
          <TextareaInput
            label="Category Description"
            name="description"
            register={register}
            errors={errors}
          />
          <ImageInput
            imageUrl={imageUrl}
            setImageUrl={setImageUrl}
            endpoint="categoryimageUploader"
            label="Catergory Image"
          />
        </div>
        <SubmitButton
          isLoading={loading}
          buttonTitle="Create Category"
          loadingButtonTitle="Creating Category please wait"
        />
      </form>
    </div>
  );
}
